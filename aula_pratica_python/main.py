#d)Faça as atribuições e, usando operadores + e * crie as saídas das variáveis
s1 = 'ant'
s2 = 'bat'
s3 = 'cod'

#1ª saída
print(s1 + ' ' + s2 + ' ' + s3)

#2ª saída
print((s1 + ' ') * 10)

#3ª saída
print(s1 + ' '+ (s2 + ' ') * 2 + ' ' + (s3 + ' ') * 3)

#4ª saída
print(((s1 + ' ') + (s2 + ' ')) * 7)

#5ª saída
